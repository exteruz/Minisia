public class Nota {
    private float valor;
    private int id;


    public Nota(float valor, int id) {
        this.valor = valor;
        this.id = id;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Nota{" + "valor=" + valor + ", id=" + id + '}';
    }
    
    
    
    
}
